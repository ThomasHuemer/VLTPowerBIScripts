SELECT Count(*) AS SalesLink
  FROM [dbo].[CUSTINVOICESALESLINK] cisl
WHERE cisl.INVOICEDATE	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
    cisl.INVOICEDATE <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT COUNT(*) AS InvoiceJour
  FROM [dbo].[CUSTINVOICEJOUR] cij
   WHERE cij.INVOICEDATE	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      cij.INVOICEDATE <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT COUNT(*) AS InvoiceTrans
  FROM [dbo].[CUSTINVOICETRANS] cit
   WHERE cit.INVOICEDATE	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      cit.INVOICEDATE <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT  COUNT(*) AS PackingSlipJour
  FROM [dbo].[CUSTPACKINGSLIPJOUR] cpj
 WHERE cpj.DELIVERYDATE	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      cpj.DELIVERYDATE <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT COUNT(*) PackingSlipTrans
  FROM [dbo].[CUSTPACKINGSLIPTRANS] cpt
   WHERE cpt.DELIVERYDATE	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      cpt.DELIVERYDATE <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT  COUNT(*) AS SalesLine
  FROM [dbo].[SALESLINE] sl
WHERE sl.CREATEDDATETIME	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      sl.CREATEDDATETIME <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

SELECT  COUNT(*) AS SalesTable
  FROM [dbo].[SALESTABLE] st
WHERE st.CREATEDDATETIME	>= DATETIMEFROMPARTS(2014, 1, 1, 0, 0, 0, 0) AND
      st.CREATEDDATETIME <= DATETIMEFROMPARTS(2017, 4, 30, 0, 0, 0, 0)

