  FROM [dbo].[CUSTINVOICEJOUR] cij
   WHERE cij.INVOICEDATE	>= DATETIMEFROMPARTS(2019, 1, 1, 0, 0, 0, 0) AND
      cij.INVOICEDATE <= DATETIMEFROMPARTS(2019, 12, 31, 0, 0, 0, 0)

