IF EXISTS (
		SELECT
			1
		FROM sys.views
		WHERE NAME = 'VLT_DB_POWERBI_ProdPool'
			AND TYPE = 'V'
	)
	DROP VIEW VLT_DB_POWERBI_ProdPool;
GO

CREATE VIEW [dbo].VLT_DB_POWERBI_ProdPool
AS

	SELECT pp.PRODPOOLID,
		pp.NAME,
		pp.NVPRODWEEKCAPACITY,
		pp.PRODPOOLID + ' ' + pp.NAME	 AS SearchPoolName
	FROM
	dbo.PRODPOOL pp
	WHERE pp.DATAAREAID	= '100'
GO

